# Modulo Exercise

print("""Welcome to the modulo operator exercise

This script will output the remainder of a division operation.


""")

enumerator = int(input("Please input the enumerator: "))
denominator = int(input("Please input the denominator: "))
modulo = enumerator % denominator
print(f"The remainder of {enumerator} divided by {denominator} equals {modulo}.")

